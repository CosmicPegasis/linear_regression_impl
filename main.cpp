class LinearRegression
{
    double dot_product(const vector<double> &v1, const vector<double> &v2)
    {
        if (v1.size() != v2.size())
            throw runtime_error("v1 and v2 don't have same dimensions.");

        return inner_product(v1.begin(), v1.end(), v2.begin(), 0.0);
    }
    double square_error(double x1, double x2)
    {
        return (x1 - x2) * (x1 - x2);
    }

    double cost_function()
    {
        if (x.size() != y.size())
            throw runtime_error("x and y don't have same dimensions.");

        vector<double> y_pred(x.size());
        transform(x.begin(), x.end(), y_pred.begin(),
                  [&](const vector<double> &x_cur) { return dot_product(x_cur, theta); });
        double cost = 0.0;
        for (size_t i = 0; i < y.size(); i++)
        {
            cost += square_error(y_pred[i], y[i]);
        }
        return cost / (2.0 * double(y.size()));
    }
    // Gives partial derivative of cost function for a given parameter
    double derivative_per_day(vector<double> x_cur, double y_cur, const int param_index)
    {
        // First calculate dot product
        const double x_theta = dot_product(x_cur, theta);
        // subtract from y;
        // multiply be appropriate parameter of x times -1
        return (y_cur - x_theta) * -1.0 * (x_cur[param_index]);
        // return the answer
    }

    // Calculates the partial derivative of J(w,b) over range of values of x and y, for a specific parameter;
    double cost_partial_derivative(const int parameter)
    {
        if (x.size() != y.size())
            throw runtime_error("x and y are not the same dimension");

        if (parameter >= x.size())
            throw runtime_error("parameter doesn't exist in x vector");

        double derivative = 0.0;
        for (size_t i = 0; i < x.size(); i++)
        {
            derivative += derivative_per_day(x[i], y[i], parameter);
        }
        return derivative / double(x.size());
    }
    // implement new value calculator
    // for every parameter we calculate its new value using cost partial derivate;
    double optimise_parameter(const int parameter_index)
    {
        return theta[parameter_index] - alpha * cost_partial_derivative(parameter_index);
    }

    vector<double> optimise_parameters(const vector<double> theta)
    {
        vector<double> new_theta(theta.size());
        for (int i = 0; i < theta.size(); i++)
        {
            new_theta[i] = optimise_parameter(i);
        }
        return new_theta;
    };

    vector<double> theta;
    const vector<vector<double>> x;
    const vector<double> y;

  public:
    LinearRegression(const vector<vector<double>> &x, const vector<double> &y) : x{x}, y{y}
    {
        if (x.size() != y.size())
            throw runtime_error("x and y don't have same dimensions");

        if (x.size() == 0)
            throw runtime_error("cannot pass empty dataset");
        theta = vector<double>(x[0].size(), 0);
        for (int i = 0; i < 1000; i++)
        {
            theta = optimise_parameters(theta);
        }
    }
    double predict(const vector<double> &x)
    {
        return dot_product(x, theta);
    }
    vector<double> get_theta()
    {
        return theta;
    }
};

